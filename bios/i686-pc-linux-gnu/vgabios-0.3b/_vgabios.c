static void biosfn_write_teletype (car, page, attr, flag) 
Bit8u car;Bit8u page;Bit8u attr;Bit8u flag;
{ 

 Bit8u xcurs,ycurs,mode,line;
 Bit16u nbcols,nbrows,address;
 Bit16u cursor,dummy;

  
 if(page==0xff)
  page=read_byte(0x40 ,0x62 );

  

  
 mode=read_byte(0x40 ,0x49 );
 line=find_vga_entry(mode);
 if(line==0xFF)return;

  
 biosfn_get_cursor_pos(page,&dummy,&cursor);
 xcurs=cursor&0x00ff;ycurs=(cursor&0xff00)>>8;

  
 nbrows=read_byte(0x40 ,0x84 )+1;
 nbcols=read_word(0x40 ,0x4A );

 switch(car)
  {
   case 7:
     
    break;

   case 8:
    if(xcurs>0)xcurs--;
    break;

   case '\r':
    xcurs=0;
    break;

   case '\n':
    xcurs=0;
    ycurs++;
    break;

   case '\t':
    do
     {
      biosfn_write_teletype(' ',page,attr,flag);
      biosfn_get_cursor_pos(page,&dummy,&cursor);
      xcurs=cursor&0x00ff;ycurs=(cursor&0xff00)>>8;
     }while(xcurs%8==0);
    break;

   default:
     
    address= (((( nbcols * nbrows *2)|0x00ff)+1)* page ) +(xcurs+ycurs*nbcols)*2;

     
    write_byte(vga_modes[line].sstart,address,car);

    if(flag== 3 )
     write_byte(vga_modes[line].sstart,address+1,attr);

    xcurs++;
  }

  
 if(xcurs==nbcols)
  {xcurs=0;
   ycurs++;
  }

  
 if(ycurs==nbrows)
  {biosfn_scroll(0x01,0x07,0,0,nbrows-1,nbcols-1,page,1 );
   ycurs-=1;
  }
 
  
 cursor=ycurs; cursor<<=8; cursor+=xcurs;
 biosfn_set_cursor_pos(page,cursor);
}

 
